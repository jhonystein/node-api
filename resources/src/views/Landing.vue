    },
    methods:{
    checkForm: function () {
        if (this.name && this.email && this.message) {
            this.errors = [];
            this.formComplete = true;
            return true;
        } 
        this.errors = [];
        if (!this.name) {
            this.errors.push('Name required.');
        }
